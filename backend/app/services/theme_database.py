import random
import copy


def pick_random_theme(r18_level: str = "hard") -> dict:
    """从主题库中随机挑选一个主题，优先选择匹配的r18_level"""
    # 先过滤匹配级别的主题
    matched = [t for t in ADULT_THEMES if isinstance(t, dict) and t.get("r18_level") == r18_level]
    if not matched:
        matched = [t for t in ADULT_THEMES if isinstance(t, dict)]
    return copy.deepcopy(random.choice(matched))


def pick_random_costume() -> dict:
    """从制服库中随机挑选一个制服"""
    return copy.deepcopy(random.choice(COSTUMES))


def pick_random_scenario() -> dict:
    """从场景库中随机挑选一个场景"""
    return copy.deepcopy(random.choice(SCENARIOS))


def pick_random_position(category: str = None) -> str:
    """从体位库中随机挑选一个体位"""
    if category and category in POSITIONS:
        return random.choice(POSITIONS[category])
    all_positions = []
    for pos_list in POSITIONS.values():
        all_positions.extend(pos_list)
    return random.choice(all_positions)


def generate_mixed_theme() -> dict:
    """生成混合主题：从不同类别中各随机选一个组合"""
    theme = pick_random_theme()
    theme["costume"] = pick_random_costume()
    theme["scenario"] = pick_random_scenario()
    return theme


def get_theme_by_id(theme_id: str) -> Optional[dict]:
    """根据ID获取主题"""
    for t in ADULT_THEMES:
        if isinstance(t, dict) and t.get("id") == theme_id:
            return copy.deepcopy(t)
    return None


def get_themes_by_category(category: str) -> list[dict]:
    """根据类别获取所有主题"""
    return [copy.deepcopy(t) for t in ADULT_THEMES if isinstance(t, dict) and t.get("category") == category]


def get_all_themes() -> list[dict]:
    """获取所有主题"""
    return [copy.deepcopy(t) for t in ADULT_THEMES if isinstance(t, dict)]


