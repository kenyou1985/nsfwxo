export function getRandomElements<T>(arr: T[], count: number, seed?: number): T[] {
  if (count >= arr.length) return [...arr].sort(() => (seed ?? Math.random()) > 0.5 ? 1 : -1);
  const result: T[] = [];
  const used = new Set<number>();
  let attempts = 0;
  while (result.length < count && attempts < count * 10) {
    const idx = Math.floor(((seed ?? Math.random()) * 10000 + attempts * 7) % arr.length);
    if (!used.has(idx)) {
      used.add(idx);
      result.push(arr[idx]);
    }
    attempts++;
  }
  return result;
}
