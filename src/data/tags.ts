export function getCategoryTags(category: TagCategory): string[] {
  if (category.subCategories) {
    return category.subCategories.flatMap((sub) => sub.tags);
  }
  return category.tags;
}

export function getSubcategoryName(category: TagCategory, subName: string, lang: DisplayLang): string {
  if (!category.subCategories) return '';
  const sub = category.subCategories.find((s) => s.name === subName);
  if (!sub) return subName;
  return lang === 'zh' ? sub.nameZh : sub.name;
}

export function getCategoryName(category: TagCategory, lang: DisplayLang): string {
  return lang === 'zh' ? category.name : category.nameEn;
}

export function getTagDisplayName(tag: string, lang: DisplayLang): string {
  if (lang === 'zh') {
    return TAG_TRANSLATIONS[tag] || tag;
  }
  return tag;
}
